SUPPORTED_LANGUAGES = ["en", "ru", "zh", "de", "es"]
DEFAULT_LANGUAGE = "en"


def get_translator(lang: str):
    """Return a translation function for the given language."""
    strings = TRANSLATIONS.get(lang, TRANSLATIONS[DEFAULT_LANGUAGE])
    fallback = TRANSLATIONS[DEFAULT_LANGUAGE]

    def t(key: str) -> str:
        return strings.get(key, fallback.get(key, key))

    return t


def detect_language(request) -> str:
    """Detect language from cookie > Accept-Language > config > default."""
    # 1. Cookie (set by Settings page)
    lang = request.cookies.get("oo-lang")
    if lang in SUPPORTED_LANGUAGES:
        return lang

    # 2. Accept-Language header
    accept = request.headers.get("Accept-Language", "")
    for part in accept.split(","):
        code = part.split(";")[0].strip().split("-")[0].lower()
        if code in SUPPORTED_LANGUAGES:
            return code

    # 3. App config language
    octo = request.app.get("octo")
    if octo and hasattr(octo, "_config"):
        cfg_lang = octo._config.language
        if cfg_lang in SUPPORTED_LANGUAGES:
            return cfg_lang

    return DEFAULT_LANGUAGE
